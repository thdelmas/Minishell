int		ft_nmatch(const char *s1, const char *s2)
{
	if (s1[0] == '\0' && s2[0] == '\0')
		return (1);
	if (s2[0] == '*' && s2[1] == '\0')
		return (1);
	if (s1[0] == '\0' && s2[0] == '*')
		return (ft_nmatch(s1, s2 + 1));
	if (s2[0] == '*')
		return (ft_nmatch(s1, s2 + 1) + ft_nmatch(s1 + 1, s2));
	if (s1[0] == s2[0])
		return (ft_nmatch(s1 + 1, s2 + 1));
	return (0);
}
